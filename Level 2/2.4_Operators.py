# Arithmetic Operators: + , - , * , / etc.

a = 7
b = 4
c = a + b
print(c)

# Assignment Operators: = , += , -= etc

a = 12-2             # Assign 12-2 in a
print(a)
b = 6
b +=3
print(b)
a -= 5
print(a)

# Comparison Operators: == , < , > , <= , >=, != etc

d = 5==5
print(d)
e = 5!=5
print(e)

# Logical Operators: and, or, not.

d = True or False
print(d)
f = True and False
print(f)
print(True and True)

g = not False           # Not operator  
print(g)